i = int(input("정수를 입력해주세요: "))

if i % 2 != 0: print(f"{i}은 2로 나누어 떨어지는 숫자가 아닙니다.")
else: print(f"{i}은 2로 나누어 떨어지는 숫자입니다.")

if i % 3 != 0: print(f"{i}은 3로 나누어 떨어지는 숫자가 아닙니다.")
else: print(f"{i}은 3로 나누어 떨어지는 숫자입니다.")

if i % 4 != 0: print(f"{i}은 4로 나누어 떨어지는 숫자가 아닙니다.")
else: print(f"{i}은 4로 나누어 떨어지는 숫자입니다.")

if i % 5 != 0: print(f"{i}은 5로 나누어 떨어지는 숫자가 아닙니다.")
else: print(f"{i}은 5로 나누어 떨어지는 숫자입니다.")